package Arrays;

public class SwitchStat {
    public static void main(String[] args) {
        int num=23;
        switch (num){
            case 1:
                System.out.println(15);
                break;
            case 2:
                System.out.println(25); break;
            case 23:
                System.out.println(23);
                break;
            default:
                System.out.println("Invalid");
        }
    }
}
